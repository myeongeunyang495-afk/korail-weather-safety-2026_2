/** 특정 시도의 시군구 목록 */
export function getSigungu(sido: string): Sigungu[] {
  return REGIONS[sido] ?? [];
}

/** 시도+시군구명으로 좌표 찾기 */
export function findRegion(sido: string, sigungu: string): Sigungu | undefined {
  return (REGIONS[sido] ?? []).find((s) => s.name === sigungu);
}
